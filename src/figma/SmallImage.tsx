type Props = {
  /** Сюжет — единственное свойство набора в Figma. */
  name: SmallImageName;
  /** Сторона квадрата в пикселях. В Figma размер задают руками: 88 / 78 / 64 / 56. */
  size?: number;
  className?: string;
};

export function SmallImage({ name, size = 64, className }: Props) {
  return (
    <div
      data-component={`Small Image · ${name}`}
      // Размер здесь единственное «магическое» число — и оно приходит пропом,
      // потому что в Figma это тоже ручной масштаб, а не токен.
      style={{ width: size, height: size, fontSize: Math.round(size / 2) }}
      className={cn(
        "flex shrink-0 select-none items-center justify-center",
        "rounded-[var(--radius-xl)] bg-[color:var(--card-bg-gray)]",
        className,
      )}
      // Иллюстрация декоративна: смысла не несёт, поэтому скрыта от читалки.
      // Сюжет продублирован в title — для того, кто собирает страницу.
      aria-hidden
      title={name}
    >
      <span aria-hidden className="leading-none">
        {EMOJI[name]}
      </span>
    </div>
  );
}
