import * as React from "react";
import { cn } from "@/lib/utils";
import { SmallImage } from "./SmallImage";

/*
  Figma: «Page Summary» (6390:4341), живой инстанс — 6455:1755.
  Единственное свойство — Platform=Desktop|Mobile. Ни цветов, ни размеров,
  ни состояний у компонента нет.

  Зелёная карточка-анонс в начале страницы: «На этой странице вы узнаете»
  плюс список пунктов. Обещание читателю, о чём материал — чтобы он за пять
  секунд решил, читать дальше или нет.

  Берём один раз на страницу, в самом начале, сразу после Hero. НЕ берём как
  обычный список по ходу текста и НЕ берём как итоговую врезку в конце —
  по смыслу и по цвету это именно вступление.

  Вкладывается в поток колонки НАПРЯМУЮ, без Section Container и без Card
  Container — так в живом шаблоне (лид и анонс — единственные блоки колонки
  без секции-обёртки). Почему так — открытый вопрос дизайнеру.

  Верхний отступ 40 (space/2xl) уже внутри — снаружи воздух добавлять не нужно.

  В слот кладут List Item:
      <PageSummary image="Скрепка">
        <ListItem>…</ListItem>
        <ListItem>…</ListItem>
      </PageSummary>

  Открытые вопросы дизайнеру (из разбора):
  1) Заголовок в макете разъехался: «На этой странице вы узнаете» на десктопе
     и «В этом модуле вы узнаете» на мобильном. Меняемый он или фиксированный —
     не зафиксировано. Здесь сделан меняемым, с десктопным значением по умолчанию.
  2) Заголовок использует стиль H3, но НЕ является инстансом Heading — поэтому
     здесь тоже не компонент Heading, а свой текст со стилем ds-h3. Если шкала
     заголовков поменяется, эти два места придётся править отдельно.
  3) Зелёный цвет зашит намертво, вариантов нет. Такой же анонс в другом цвете
     будет новым компонентом.
  4) На внешней обёртке в Figma стоит скругление radius/xl 32, но фона у неё нет —
     свойство ни на что не влияет, похоже на остаток от прошлой версии. Не переносим.
  5) Между пунктами своего промежутка нет — воздух даёт только верхний отступ 4
     самого List Item, поэтому список идёт плотно (шаг 29). Свой gap не добавляем.
*/

type Props = {
  /** Заголовок карточки. По умолчанию — как в десктопном макете. */
  title?: React.ReactNode;
  /** Пункты списка — List Item. В Figma это слот «Slot» шириной 704. */
  children?: React.ReactNode;
  /**
   * Сюжет декоративной картинки Small Image 78×78 в правом верхнем углу.
   * Опционально: в Figma картинка есть всегда (скрепка), но смысла не несёт —
   * текст без неё читается полностью.
   */
  image?: React.ComponentProps<typeof SmallImage>["name"];
  className?: string;
};

export function PageSummary({ title = "На этой странице вы узнаете", children, image, className }: Props) {
  return (
    // Внешняя обёртка — «строка в потоке страницы»: верхний отступ space/2xl 40.
    <div
      data-component="Page Summary"
      className={cn("w-full pt-[var(--space-2xl)]", className)}
    >
      {/*
        Content: зелёная карточка. ВАЖНО — overflow сверху резать нельзя:
        картинка выступает за верхний край на 20, иначе обрежется.
      */}
      <div className="relative rounded-[var(--radius-l)] bg-[color:var(--card-bg-green)] p-[var(--space-l)]">
        <div className="flex flex-col gap-[var(--space-l)]">
          {/* Стиль Desktop/H3 — но не инстанс Heading, см. шапку файла. */}
          <h2 className="ds-h3 text-[color:var(--text-primary)]">{title}</h2>

          {/* Slot */}
          <ul className="flex list-none flex-col">{children}</ul>
        </div>

        {image ? (
          // В Figma: 78×78, y=−20 (выступает за верхний край), 41 от правого края.
          // Токена на 41 нет — значение прямое. −20 это space/ml.
          <div
            aria-hidden
            className="pointer-events-none absolute right-[41px] top-[calc(var(--space-ml)*-1)]"
          >
            <SmallImage name={image} size={78} />
          </div>
        ) : null}
      </div>
    </div>
  );
}
