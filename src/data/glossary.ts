export const glossary: GlossaryEntry[] = [...abbreviations, ...terms];

/** Нормализация ключа: нижний регистр, без скобочного хвоста, схлопнутые пробелы. */
function normalizeKey(s: string): string {
  return s
    .toLowerCase()
    .replace(/\([^)]*\)/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

const lookup = new Map<string, GlossaryEntry>();
for (const entry of glossary) {
  lookup.set(normalizeKey(entry.term), entry);
}

/** Поиск статьи по тексту термина (для GlossaryTerm). null — если не найдено. */
export function findGlossaryEntry(term: string): GlossaryEntry | null {
  return lookup.get(normalizeKey(term)) ?? null;
}
