import { normalizeDoc, type Doc, type Node } from "./contentTree";

/*
  ЭТАЛОННАЯ СТРАНИЦА для разработчика.

  Задача — не «показать компоненты списком», а дать реальную страницу, в которой
  встречается всё: каждый вид узла, каждый значимый вариант и, главное,
  ВЛОЖЕННОСТЬ. Разработчик берёт JSON, собирает страницу в конструкторе и
  сравнивает с превью справа. Сошлось — формат понят верно, и на боевых модулях
  сюрпризов не будет.

  Контент взят из предметной области (инклюзивное трудоустройство), а не
  «Lorem ipsum»: на осмысленном тексте сразу видно, если компонент выбран не по
  назначению или отступы легли не так.

  ПРАВИЛА РАСКЛАДКИ, которые эталон демонстрирует (КОМПОНЕНТЫ.md):
  · Heading и Text кладутся ПРЯМО в Section Container;
  · всё, что не проза, заворачивается в Card Container — даже одиночный блок;
  · Page Summary — один раз на страницу, в начале, ВНЕ секций, в слоте List Item;
  · List Container — не «список», а вертикальный стек: в нём бывают и цитаты;
  · свои отступы не добавляем, они уже внутри компонентов.

  Инлайн-разметка (**жирный**, *курсив*, ссылка) расставлена намеренно: в
  выгрузке она превращается в теги, и это тоже надо проверить.

  ЧЕГО ЗДЕСЬ НЕТ: Compare, Quiz и Card Button. Они есть в библиотеке, но
  раскладка их пока не собирает — в JSON они не появятся, и обещать их эталоном
  нельзя.
*/

// ── мелкие помощники, чтобы описание страницы читалось, а не тонуло в скобках ──

const h = (level: "H2" | "H3" | "H4" | "H5", text: string): Node => ({
  component: "Heading",
  level,
  text,
});

const t = (size: "XL" | "L" | "M" | "S", text: string): Node => ({
  component: "Text",
  size,
  text,
});

const li = (
  text: string,
  type: "Dot" | "Icon" | "Number" = "Dot",
  size: "L" | "M" = "L",
): Node => ({ component: "List Item", size, type, text });

const list = (children: Node[], ordered = false): Node => ({
  component: "List Container",
  ordered,
  children,
});

/** Конверт: всё, что не проза, живёт внутри Card Container. */
const box = (
  children: Node[],
  orientation: "Vertical" | "Horizontal" = "Vertical",
): Node => ({ component: "Card Container", orientation, children });

const card = (
  bgColor: string,
  title: string | undefined,
  children: Node[],
  icon?: string,
): Node => ({
  component: "General Card",
  orient: "Vertical",
  bgColor,
  icon,
  title,
  children,
});

